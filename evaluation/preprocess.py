#!/usr/bin/env python3
# -*- coding: utf-8 -*-

"""
Preprocess an event log CSV for AgentSimulator.

It keeps only the necessary columns:
    case_id, activity, start_timestamp, end_timestamp, resource

The output filename is automatically generated by adding "_processed"
before the file extension.

Example:
    input:  production_train.csv
    output: production_train_processed.csv

Usage:
    python preprocess_for_agentsim.py --input production_train.csv

python preprocess.py \
    --input ../AgentSimulator/raw_data/Production_Data.csv \
    --case-col "Case ID" \
    --activity-col Activity \
    --resource-col Resource \
    --end-col "Complete Timestamp" \
    --start-col "Start Timestamp"
"""

from __future__ import annotations

import argparse
import sys
from pathlib import Path

import pandas as pd


def build_output_path(input_path: Path) -> Path:
    return input_path.with_name(f"{input_path.stem}_processed{input_path.suffix}")


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Keep only AgentSimulator-required columns and save as *_processed.csv."
    )

    parser.add_argument(
        "--input",
        required=True,
        help="Input CSV file path.",
    )

    parser.add_argument(
        "--case-col",
        default="case_id",
        help="Case ID column name. Default: case_id.",
    )

    parser.add_argument(
        "--activity-col",
        default="activity",
        help="Activity column name. Default: activity.",
    )

    parser.add_argument(
        "--start-col",
        default="start_timestamp",
        help="Start timestamp column name. Default: start_timestamp.",
    )

    parser.add_argument(
        "--end-col",
        default="end_timestamp",
        help="End timestamp column name. Default: end_timestamp.",
    )

    parser.add_argument(
        "--resource-col",
        default="resource",
        help="Resource column name. Default: resource.",
    )

    parser.add_argument(
        "--sep",
        default=",",
        help="CSV separator. Default: comma.",
    )

    parser.add_argument(
        "--encoding",
        default="utf-8",
        help="CSV encoding. Default: utf-8.",
    )

    args = parser.parse_args()

    input_path = Path(args.input)

    if not input_path.exists():
        print(f"ERROR: input file does not exist: {input_path}", file=sys.stderr)
        return 1

    if not input_path.is_file():
        print(f"ERROR: input path is not a file: {input_path}", file=sys.stderr)
        return 1

    output_path = build_output_path(input_path)

    try:
        df = pd.read_csv(input_path, sep=args.sep, encoding=args.encoding)

        required_cols = [
            args.case_col,
            args.activity_col,
            args.start_col,
            args.end_col,
            args.resource_col,
        ]

        missing_cols = [col for col in required_cols if col not in df.columns]

        if missing_cols:
            print(
                f"ERROR: missing required columns: {missing_cols}\n"
                f"Available columns: {list(df.columns)}",
                file=sys.stderr,
            )
            return 1

        processed = df[required_cols].copy()

        processed = processed.rename(
            columns={
                args.case_col: "case_id",
                args.activity_col: "activity",
                args.start_col: "start_timestamp",
                args.end_col: "end_timestamp",
                args.resource_col: "resource",
            }
        )

        processed["start_timestamp"] = pd.to_datetime(
            processed["start_timestamp"],
            utc=True,
            format="mixed",
            errors="coerce",
        )

        processed["end_timestamp"] = pd.to_datetime(
            processed["end_timestamp"],
            utc=True,
            format="mixed",
            errors="coerce",
        )

        invalid_time_rows = (
            processed["start_timestamp"].isna()
            | processed["end_timestamp"].isna()
        ).sum()

        if invalid_time_rows > 0:
            print(
                f"WARNING: {invalid_time_rows} rows have unparseable timestamps and will be dropped.",
                file=sys.stderr,
            )
            processed = processed.dropna(
                subset=["start_timestamp", "end_timestamp"]
            ).copy()

        processed["case_id"] = processed["case_id"].astype(str)
        processed["activity"] = processed["activity"].astype(str)
        processed["resource"] = processed["resource"].fillna("UNKNOWN").astype(str)

        processed = processed.sort_values(
            ["case_id", "start_timestamp", "end_timestamp"],
            kind="mergesort",
        ).reset_index(drop=True)

        processed.to_csv(output_path, index=False)

        print(f"Input file:  {input_path}")
        print(f"Output file: {output_path}")
        print(f"Rows saved:  {len(processed)}")
        print(f"Columns:     {list(processed.columns)}")

        return 0

    except Exception as e:
        print(f"ERROR: {e}", file=sys.stderr)
        return 1


if __name__ == "__main__":
    raise SystemExit(main())